static double		fade(double t)
{
  return (t * t * t * (t * (t * 6 - 15) + 10));
}

static double		lerp(double t, double a, double b)
{
  return (a + t * (b - a));
}

static double		grad(int hash, double x, double y, double z)
{
  int			h;
  double		u;
  double		v;

  h = hash & 15;
  u = (h < 8 || h == 12 || h == 13) ? x : y;
  v = (h < 4 || h == 12 || h == 13) ? y : z;
  return ((((h & 1) == 0) ? u : -u) + (((h & 2) == 0) ? v : -v));
}

static void	       calc_coord(int x, int y, int z, int *coord)
{
  coord[0] = x & 255;
  coord[1] = y & 255;
  coord[2] = z & 255;
}

double			noise(double x, double y, double z)
{
  int			coord[3];
  double		u[2];
  int			tmp[4];

  calc_coord((int) x, (int) y, (int) z, coord);
  x -= floor(x);
  y -= floor(y);
  z -= floor(z);
  u[0] = fade(x);
  u[1] = fade(y);
  tmp[1] = perlin[coord[0]] + coord[1];
  tmp[0] = perlin[tmp[1]] + coord[2];
  tmp[1] = perlin[tmp[1] + 1] + coord[2];
  tmp[3] = perlin[coord[0] + 1] + coord[1];
  tmp[2] = perlin[tmp[3]] + coord[2];
  tmp[3] = perlin[tmp[3] + 1] + coord[2];
  return (lerp(fade(z), lerp(u[1], lerp(u[0], grad(perlin[tmp[0]], x, y, z),
					grad(perlin[tmp[2]], x - 1, y, z)),
			     lerp(u[0], grad(perlin[tmp[1]], x, y - 1, z),
				  grad(perlin[tmp[3]], x - 1, y - 1, z))),
	       lerp(u[1], lerp(u[0], grad(perlin[tmp[0] + 1], x, y, z - 1),
			       grad(perlin[tmp[2] + 1], x - 1, y, z - 1)),
		    lerp(u[0], grad(perlin[tmp[1] + 1], x, y - 1, z - 1),
			 grad(perlin[tmp[3] + 1], x - 1, y - 1, z - 1)))));
}
